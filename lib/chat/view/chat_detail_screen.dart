            physics: NeverScrollableScrollPhysics(),
            itemBuilder: (context, index){
              return Container(
                padding: EdgeInsets.only(left: 14,right: 14,top: 10,bottom: 10),
                child: Align(
                  alignment: (chatBloc.messages[index].messageType == "${stringConstant.receiver}"?Alignment.topLeft:Alignment.topRight),
